// Scans src/shots/**/*.tsx, extracts each file's compositionConfig, and writes:
//   src/registry.gen.tsx     — imports every shot for Root.tsx
//   src/shots.manifest.json  — metadata for the bulk renderer
// Run before `remotion studio` / render. VidTSX-skill files drop in and are picked up automatically.
import { readdirSync, readFileSync, writeFileSync, statSync } from 'fs';
import { fileURLToPath } from 'url';
import path from 'path';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const shotsDir = path.join(root, 'src', 'shots');

// Optional `--type=<longs|channel|faceless|lectures>` narrows the registry to one video type's
// shots, so each workspace's Studio only shows its own (see MIGRATION.md). Maps the shot GROUP
// folder (src/shots/<group>/) to a type by prefix; unknown groups fall through to 'other'.
function typeOfGroup(g) {
  if (/^video-/.test(g)) return 'channel-longs';
  if (/^short-/.test(g)) return 'faceless';
  if (/^ch-/.test(g) || g === 'cover') return 'channel';
  if (/^bwa-/.test(g) || g === 'ebq') return 'lectures';
  return 'other';
}
const typeArg = (process.argv.find((a) => a.startsWith('--type=')) || '').split('=')[1] || null;

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = path.join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (name.endsWith('.tsx')) out.push(p);
  }
  return out;
}

function parseConfig(src) {
  const block = /export\s+const\s+compositionConfig\s*=\s*{([\s\S]*?)}\s*;/.exec(src);
  if (!block) return null;
  const body = block[1];
  const str = (k) => { const m = new RegExp(k + "\\s*:\\s*['\"]([^'\"]+)['\"]").exec(body); return m ? m[1] : undefined; };
  const num = (k) => { const m = new RegExp(k + "\\s*:\\s*([0-9.]+)").exec(body); return m ? Number(m[1]) : undefined; };
  const bool = (k) => new RegExp(k + "\\s*:\\s*true").test(body);
  const id = str('id');
  if (!id) return null;
  return {
    id,
    durationInSeconds: num('durationInSeconds') ?? 5,
    fps: num('fps') ?? 30,
    width: num('width') ?? 1920,
    height: num('height') ?? 1080,
    transparent: bool('transparent'),
  };
}

let files = [];
try { files = walk(shotsDir); } catch { files = []; }

const shots = [];
const seen = new Set();
for (const file of files.sort()) {
  const group = path.relative(shotsDir, file).split(path.sep)[0];
  if (typeArg && typeOfGroup(group) !== typeArg) continue;
  const cfg = parseConfig(readFileSync(file, 'utf8'));
  if (!cfg) { console.warn('  skip (no compositionConfig):', path.relative(root, file)); continue; }
  if (seen.has(cfg.id)) { console.warn('  DUPLICATE id, skipping:', cfg.id, path.relative(root, file)); continue; }
  seen.add(cfg.id);
  let imp = './' + path.relative(path.join(root, 'src'), file).replace(/\\/g, '/').replace(/\.tsx$/, '');
  shots.push({ ...cfg, imp });
}

const header = '// AUTO-GENERATED by scripts/gen-registry.mjs — do not edit.\n';
let reg = header + "import type React from 'react';\n";
shots.forEach((s, i) => { reg += `import Shot${i}, { compositionConfig as cfg${i} } from '${s.imp}';\n`; });
reg += '\nexport const shots: { Comp: React.FC; config: any }[] = [\n';
shots.forEach((s, i) => { reg += `  { Comp: Shot${i} as React.FC, config: cfg${i} },\n`; });
reg += '];\n';
writeFileSync(path.join(root, 'src', 'registry.gen.tsx'), reg);

writeFileSync(
  path.join(root, 'src', 'shots.manifest.json'),
  JSON.stringify(shots.map(({ imp, ...m }) => m), null, 1)
);

console.log(`gen-registry: ${shots.length} shot(s)${typeArg ? ` [type=${typeArg}]` : ''} -> registry.gen.tsx`);
shots.forEach((s) => console.log(`  - ${s.id}  ${s.width}x${s.height}@${s.fps} ${s.durationInSeconds}s${s.transparent ? ' [alpha]' : ''}`));
